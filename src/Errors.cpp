#include "Errors.h"

#include <queue>

///////////////////////////////////////////////////////////////////////////////
//ErrorThrower                                                               //
///////////////////////////////////////////////////////////////////////////////
Error ErrorThrower::getError() {
  if (mErrors.empty()) {
    return ERR_NO_ERROR;
  } else {
    Error temp = mErrors.front();
    mErrors.pop();
    return temp;
  }
}
///////////////////////////////////////////////////////////////////////////////
bool ErrorThrower::hasError() {
  return !mErrors.empty();
}
///////////////////////////////////////////////////////////////////////////////
void ErrorThrower::addError(Error error) {
  mErrors.push_back(error);
}
